public class Allcompaniesadapter {
    public String name;
    public String link;
    public String info;
    public String contactperson;
    public String contactnumber;
    public String contactemail;

    public Allcompaniesadapter()
    {

    }
    public Allcompaniesadapter(String name,String link,String info,String contactperson,String contactnumber,String contactemail)
    {
        this.name=name;
        this.link=link;
        this.info=info;
        this.contactperson=contactperson;
        this.contactnumber=contactnumber;
        this.contactemail=contactemail;


    }

}
